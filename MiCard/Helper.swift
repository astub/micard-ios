import UIKit

class Helper: NSObject {
    
    func blueColor() -> UIColor {
        return UIColor(red: 27/255.0, green: 146/255.0, blue: 178/255.0, alpha: 1.0)
    }
    
    func redColor() -> UIColor {
        return UIColor(red: 178/255.0, green: 32/255.0, blue: 27/255.0, alpha: 1.0)
    }
    
    func greenColor() -> UIColor {
        return UIColor(red: 85/255.0, green: 159/255.0, blue: 61/255.0, alpha: 1.0)
    }
   
}
